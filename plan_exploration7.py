# Yithian (State 57)
# Yithians wander the grid. They have a special ability to project their minds through time and space.
# They select a target random basic lifeform (0-4), Investigator (39) or Cultist (40) anywhere on the grid.
# The target is instantly swapped with a Yithian (57) and the original Yithian becomes the target's original state (Mind Swap).
# This happens with a 2% chance per turn. Otherwise, they wander randomly.

# Flying Polyp (State 58)
# An ancient enemy of the Yithians.
# Flying Polyps spawn rarely (0.005%).
# They wander the grid aggressively.
# If they encounter a Yithian (57), they instantly destroy it, leaving a Void (6).
# If they encounter basic lifeforms (0-4) or Cultists (40), they also destroy them into Void (6).
# They have a 1% chance per turn to spawn a new Flying Polyp in an adjacent Void.
